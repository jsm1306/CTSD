#include<stdio.h>
struct time
{
	int hr;
	int min;
	int sec;
};
int main()
{
	struct time t1,t2;
	printf("Enter Starting time: \n");
	scanf("%d",&t1.hr);
	scanf("%d",&t1.min);
	scanf("%d",&t1.sec);
	printf("Enter Ending time: \n");
	scanf("%d",&t2.hr);
	scanf("%d",&t2.min);
	scanf("%d",&t2.sec);
	
}
